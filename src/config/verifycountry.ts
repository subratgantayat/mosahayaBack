const toCamelCase = (str): string => {
    return str
        .replace(/\s(.)/g, function ($1) {
            return $1.toUpperCase();
        })
        .replace(/\s/g, '')
        .replace(/^(.)/, function ($1) {
            return $1.toLowerCase();
        });
};

const hasLowerCase = (str: string) => {
    return (/[a-z]/.test(str));
};
const hasUpperCase = (str: string) => {
    return (/[A-Z]/.test(str));
};

