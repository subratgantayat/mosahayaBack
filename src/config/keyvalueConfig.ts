const getValueArray = (name: string): string[] => {
    const returnValue: string[] = [];
    if (keyvalue[name]) {
        for (const a of keyvalue[name]) {
            returnValue.push(a.value);
        }
    }
    return returnValue;
};

const getSkillArray = (sectors: string[]): string[] => {
    console.log(sectors);
    const returnValue: string[] = [];
    for (const inSec of sectors) {
        for (const sec of keyvalue.skillsBySector) {
            if (sec.value === inSec) {
                for (const skill of sec.skills) {
                    returnValue.push(skill.value);
                }
                break;
            }
        }
    }
    return returnValue;
};

const getCityArray = (): string[] => {
    const returnValue: string[] = [];
    if (keyvalue.city) {
        for (const a of keyvalue.city) {
            returnValue.push(a.City);
        }
    }
    return returnValue;
};

const getStateArray = (country: string): string[] => {
    const returnValue: string[] = [];
    for (const a of keyvalue.country) {
        if (a.value === country) {
            for (const s of a.states) {
                returnValue.push(s.value);
            }
            break;
        }
    }
    return returnValue;
};

const getDistrictArray = (country: string, state: string): string[] => {
    const returnValue: string[] = [];
    for (const a of keyvalue.country) {
        if (a.value === country) {
            for (const s of a.states) {
                if (s.value === state) {
                    for (const d of s.districts) {
                        returnValue.push(d.value);
                    }
                    break;
                }
            }
            break;
        }
    }
    return returnValue;
};

